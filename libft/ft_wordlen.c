#include "includes/libft.h"

int	ft_wordlen(const char *s, char c)
{
	int i;
	int l;

	i = 0;
	l = 0;
	while (s[i])
	{
		if (s[i] == c)
			i++;
		if (s[i] != c)
		{
			i++;
			l++;
		}
	}
	return (l);
}
