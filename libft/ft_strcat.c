#include "includes/libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int i;
	int j;

	i = -1;
	j = -1;
	while (s1[++i] != '\0')
		;
	while (s2[++j] != '\0')
		s1[i++] = s2[j];
	s1[i] = '\0';
	return (s1);
}
